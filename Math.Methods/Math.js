//round()
const number1 = Math.round(3.635)
console.log(number1);
//ceil()
const number2 = Math.ceil(3.235)
console.log(number2);
//floor()
const number3 = Math.floor(3.635)
console.log(number3);
//abs() absolute value of an integer
const number4 = Math.abs(-7)
console.log(number4);
//random
const number5 = Math.random()
console.log(number5)
//min() minimum value
const number6 = Math.min(3,5,73,1,0,-2)
console.log(number6)
//max() maximum value
const number7 = Math.max(5,35,32,52,4,1,-1)
console.log(number7)
//pow() power of the number
const number8 = Math.pow(2,3) // 2 is the number and 3 is the power
console.log(number8)
//sqrt() squareroot of a number
const number9 = Math.sqrt(16)
console.log(number9)
//PI to use the value of PI
const PI = Math.PI
console.log(PI);




